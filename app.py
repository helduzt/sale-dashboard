uploaded_file = st.file_uploader("1. ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏•‡∏≤‡∏Å‡πÑ‡∏ü‡∏•‡πå‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• (CSV ‡∏´‡∏£‡∏∑‡∏≠ Excel) ‡∏°‡∏≤‡∏ß‡∏≤‡∏á‡∏ó‡∏µ‡πà‡∏ô‡∏µ‡πà", type=['csv', 'xlsx', 'xls'])

# --- 3. ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏• ---
@st.cache_data
def load_data(file):
    df = None
    # ‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡πÑ‡∏ü‡∏•‡πå CSV
    if file.name.lower().endswith('.csv'):
        # ‡∏•‡∏≠‡∏á‡∏≠‡πà‡∏≤‡∏ô‡∏ó‡∏µ‡∏•‡∏∞‡∏†‡∏≤‡∏©‡∏≤ (‡∏Å‡∏±‡∏ô‡∏†‡∏≤‡∏©‡∏≤‡∏ï‡πà‡∏≤‡∏á‡∏î‡∏≤‡∏ß)
        encodings = ['utf-8', 'cp874', 'tis-620', 'utf-16']
        for enc in encodings:
            try:
                file.seek(0) # ‡∏£‡∏µ‡πÄ‡∏ã‡πá‡∏ï‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏õ‡∏ó‡∏µ‡πà‡∏à‡∏∏‡∏î‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô
                df = pd.read_csv(file, encoding=enc)
                break
            except:
                continue
                
    # ‡∏ñ‡πâ‡∏≤‡πÄ‡∏õ‡πá‡∏ô‡πÑ‡∏ü‡∏•‡πå Excel
    else:
        try:
            df = pd.read_excel(file)
        except Exception as e:
            st.error(f"‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ü‡∏•‡πå Excel ‡πÑ‡∏°‡πà‡πÑ‡∏î‡πâ: {e}")
            return None

    if df is None:
        return None

    # --- Clean Data ---
    try:
        # ‡∏•‡∏ö‡∏ä‡πà‡∏≠‡∏á‡∏ß‡πà‡∏≤‡∏á‡∏ä‡∏∑‡πà‡∏≠ Column
        df.columns = df.columns.str.strip()
        
        # ‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£ (PERSONID) ‡πÉ‡∏´‡πâ‡πÄ‡∏õ‡πá‡∏ô‡∏Ç‡πâ‡∏≠‡∏Ñ‡∏ß‡∏≤‡∏°
        # ‡∏´‡∏≤ column ‡∏ó‡∏µ‡πà‡∏ô‡πà‡∏≤‡∏à‡∏∞‡πÄ‡∏õ‡πá‡∏ô‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£ (‡∏ó‡∏µ‡πà‡∏°‡∏µ‡∏Ñ‡∏≥‡∏ß‡πà‡∏≤ ID ‡∏´‡∏£‡∏∑‡∏≠ PERSON)
        id_col = [c for c in df.columns if 'PERSON' in c or 'ID' in c][0]
        df['Search_ID'] = df[id_col].astype(str).str.replace(r'[^0-9]', '', regex=True)
        
        # ‡∏´‡∏≤ column ‡∏ä‡∏∑‡πà‡∏≠ (FNAME)
        name_col = [c for c in df.columns if 'NAME' in c or 'FNAME' in c][0]
        df['Search_Name'] = df[name_col].astype(str).fillna('')
        
        # ‡∏£‡∏ß‡∏°‡∏ä‡∏∑‡πà‡∏≠‡∏ô‡∏≤‡∏°‡∏™‡∏Å‡∏∏‡∏• (‡∏ñ‡πâ‡∏≤‡∏°‡∏µ LNAME)
        lname_col = [c for c in df.columns if 'LNAME' in c]
        if lname_col:
            df['Search_Name'] += ' ' + df[lname_col[0]].astype(str).fillna('')

    except Exception as e:
        st.error(f"‚ùå ‡∏£‡∏π‡∏õ‡πÅ‡∏ö‡∏ö‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏°‡πà‡∏ñ‡∏π‡∏Å‡∏ï‡πâ‡∏≠‡∏á: {e}")
        return None

    return df

# --- 4. ‡∏Å‡∏≤‡∏£‡∏ó‡∏≥‡∏á‡∏≤‡∏ô‡∏´‡∏•‡∏±‡∏Å ---
if uploaded_file is not None:
    # ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
    df = load_data(uploaded_file)
    
    if df is not None:
        st.success(f"‚úÖ ‡πÇ‡∏´‡∏•‡∏î‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à! ‡∏û‡∏ö {len(df):,} ‡∏£‡∏≤‡∏¢‡∏Å‡∏≤‡∏£")
        
        # ‡πÅ‡∏ö‡πà‡∏á‡∏´‡∏ô‡πâ‡∏≤‡∏à‡∏≠ ‡∏ã‡πâ‡∏≤‡∏¢(‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤) ‡∏Ç‡∏ß‡∏≤(‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•)
        col1, col2 = st.columns([1, 2])
        
        with col1:
            st.subheader("üîç 2. ‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤")
            search_text = st.text_input("‡∏û‡∏¥‡∏°‡∏û‡πå‡∏ä‡∏∑‡πà‡∏≠ ‡∏´‡∏£‡∏∑‡∏≠ ‡πÄ‡∏ö‡∏≠‡∏£‡πå‡πÇ‡∏ó‡∏£", placeholder="‡πÄ‡∏ä‡πà‡∏ô 081...")
            
            selected_person = None
            
            if search_text:
                # ‡∏Å‡∏£‡∏≠‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•
                mask = (df['Search_ID'].str.contains(search_text, na=False)) | \
                       (df['Search_Name'].str.contains(search_text, na=False))
                results = df[mask]
                
                # ‡πÄ‡∏≠‡∏≤‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏ó‡∏µ‡πà‡πÑ‡∏°‡πà‡∏ã‡πâ‡∏≥‡∏°‡∏≤‡πÅ‡∏™‡∏î‡∏á
                people = results[['Search_ID', 'Search_Name']].drop_duplicates()
                
                if not people.empty:
                    st.info(f"‡∏û‡∏ö {len(people)} ‡∏Ñ‡∏ô")
                    # ‡∏™‡∏£‡πâ‡∏≤‡∏á Radio Button ‡πÉ‡∏´‡πâ‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
                    choice = st.radio(
                        "‡∏ú‡∏•‡∏Å‡∏≤‡∏£‡∏Ñ‡πâ‡∏ô‡∏´‡∏≤ (‡∏Ñ‡∏•‡∏¥‡∏Å‡πÄ‡∏û‡∏∑‡πà‡∏≠‡∏î‡∏π‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•)",
                        people.apply(lambda x: f"{x['Search_Name']} ({x['Search_ID']})", axis=1)
                    )
                    # ‡πÅ‡∏Å‡∏∞ ID ‡∏à‡∏≤‡∏Å‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏∑‡∏≠‡∏Å
                    selected_id = choice.split('(')[-1].replace(')', '')
                    selected_person = selected_id
                else:
                    st.warning("‡πÑ‡∏°‡πà‡∏û‡∏ö‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•")

        with col2:
            st.subheader("üìä 3. ‡∏õ‡∏£‡∏∞‡∏ß‡∏±‡∏ï‡∏¥‡∏Å‡∏≤‡∏£‡∏ã‡∏∑‡πâ‡∏≠")
            
            if selected_person:
                # ‡∏î‡∏∂‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏Ñ‡∏ô‡∏ô‡∏±‡πâ‡∏ô
                history = df[df['Search_ID'] == selected_person]
                
                # ‡πÅ‡∏™‡∏î‡∏á‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡∏™‡πà‡∏ß‡∏ô‡∏ï‡∏±‡∏ß
                info = history.iloc[0]
                st.markdown(f"**‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤:** {info['Search_Name']}")
                st.markdown(f"**‡πÄ‡∏ö‡∏≠‡∏£‡πå:** {selected_person}")
                
                # ‡∏Ñ‡∏≥‡∏ô‡∏ß‡∏ì‡∏¢‡∏≠‡∏î‡∏£‡∏ß‡∏° (‡∏´‡∏≤ column ‡∏ó‡∏µ‡πà‡πÄ‡∏õ‡πá‡∏ô‡∏ï‡∏±‡∏ß‡πÄ‡∏•‡∏Ç‡∏¢‡∏≠‡∏î‡πÄ‡∏á‡∏¥‡∏ô)
                amount_col = [c for c in df.columns if 'AMOUNT' in c or 'PRICE' in c or 'TOTAL' in c]
                if amount_col:
                    total = history[amount_col[-1]].sum()
                    st.metric("üí∞ ‡∏¢‡∏≠‡∏î‡∏ã‡∏∑‡πâ‡∏≠‡∏£‡∏ß‡∏°‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î", f"‡∏ø{total:,.2f}")
                
                # ‡πÅ‡∏™‡∏î‡∏á‡∏ï‡∏≤‡∏£‡∏≤‡∏á
                st.dataframe(history, use_container_width=True)
            else:
                st.info("üëà ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡πÄ‡∏•‡∏∑‡∏≠‡∏Å‡∏£‡∏≤‡∏¢‡∏ä‡∏∑‡πà‡∏≠‡∏•‡∏π‡∏Å‡∏Ñ‡πâ‡∏≤‡∏à‡∏≤‡∏Å‡∏î‡πâ‡∏≤‡∏ô‡∏ã‡πâ‡∏≤‡∏¢")

    else:
        st.error("‡∏≠‡πà‡∏≤‡∏ô‡πÑ‡∏ü‡∏•‡πå‡πÑ‡∏°‡πà‡∏™‡∏≥‡πÄ‡∏£‡πá‡∏à ‡∏•‡∏≠‡∏á Save ‡πÑ‡∏ü‡∏•‡πå‡πÄ‡∏õ‡πá‡∏ô CSV (UTF-8) ‡πÅ‡∏•‡πâ‡∏ß‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÉ‡∏´‡∏°‡πà‡∏Ñ‡∏£‡∏±‡∏ö")

else:
    # ‡∏ï‡∏≠‡∏ô‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå
    st.info("üëÜ ‡∏Å‡∏£‡∏∏‡∏ì‡∏≤‡∏≠‡∏±‡∏õ‡πÇ‡∏´‡∏•‡∏î‡πÑ‡∏ü‡∏•‡πå‡∏Ç‡πâ‡∏≠‡∏°‡∏π‡∏•‡πÄ‡∏û‡∏∑‡πà‡∏≠‡πÄ‡∏£‡∏¥‡πà‡∏°‡∏ï‡πâ‡∏ô")
